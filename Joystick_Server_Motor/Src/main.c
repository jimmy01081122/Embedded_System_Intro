/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
  HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);

  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4); // 改為啟動 TIM4
  uint32_t servo_pulse;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	  // --- 讀取第一通道 (Rank 1: VRX) ---
	      HAL_ADC_Start(&hadc1);
	      HAL_ADC_PollForConversion(&hadc1, 10);
	      adc_val[0] = HAL_ADC_GetValue(&hadc1); // X 軸數值

	      // --- 讀取第二通道 (Rank 2: VRY) ---
	      HAL_ADC_Start(&hadc1);
	      HAL_ADC_PollForConversion(&hadc1, 10);
	      adc_val[1] = HAL_ADC_GetValue(&hadc1); // Y 軸數值

	      HAL_ADC_Stop(&hadc1);

	      // --- 讀取 Z 軸按鍵 (PA4) ---
	      btn_z = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_4);





	      // --- 伺服馬達控制 (使用 X 軸 adc_val[0]) ---
	      // 公式：500 (0.5ms) 為 0 度，2500 (2.5ms) 為 180 度
	      // 映射範圍：0~4095 -> 500~2500
	      int32_t offset_x = (int32_t)adc_val[0] - 2048; // 範圍約 -2048 ~ 2047
	      int32_t offset_y = (int32_t)adc_val[1] - 2048;

	      // 取絕對值較大者作為控制量
	      int32_t max_offset = 0;

	      if (abs(offset_x) > abs(offset_y)) {
	           max_offset = offset_x;
	       } else {
	           max_offset = offset_y;
	       }
	      if (abs(max_offset) < 50) max_offset = 0; // 忽略微小偏移
	      servo_pulse = 1500 + (max_offset * 1000 / 2048);

//	      uint32_t servo_pulse = 500 + ((uint32_t)adc_val[0] * 2000 / 4095);

	      // 更新 PWM 輸出至 PB8 (TIM4 Channel 3)
	      __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_4, servo_pulse);

	      // --- 格式化字串並透過 UART 輸出 ---
	      // 加入 PWM 數值方便除錯
	      sprintf(uart_buf, "X: %4d | Y: %4d | Z: %d | PWM: %ld\r\n",
	              adc_val[0], adc_val[1], btn_z, servo_pulse);
	      HAL_UART_Transmit(&huart2, (uint8_t*)uart_buf, strlen(uart_buf), 100);

	      // --- 延遲 ---
	      // 建議 20ms 以符合伺服馬達 50Hz 更新率，若 UART 輸出太頻繁可調高
	      HAL_Delay(20);
  }
  /* USER CODE END 3 */

}

